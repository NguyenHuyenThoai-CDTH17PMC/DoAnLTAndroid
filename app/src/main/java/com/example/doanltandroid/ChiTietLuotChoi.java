package com.example.doanltandroid;

public class ChiTietLuotChoi {
     String luot_choi_id;
     String cau_hoi_id;
     String phuong_an;
     String diem;


    public String getLuot_choi_id() {
        return luot_choi_id;
    }

    public void setLuot_choi_id(String luot_choi_id) {
        this.luot_choi_id = luot_choi_id;
    }

    public String getCau_hoi_id() {
        return cau_hoi_id;
    }

    public void setCau_hoi_id(String cau_hoi_id) {
        this.cau_hoi_id = cau_hoi_id;
    }

    public String getPhuong_an() {
        return phuong_an;
    }

    public void setPhuong_an(String phuong_an) {
        this.phuong_an = phuong_an;
    }

    public String getDiem() {
        return diem;
    }

    public void setDiem(String diem) {
        this.diem = diem;
    }
}
